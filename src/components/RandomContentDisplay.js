function RandomContentDisplay() {
  const [selectedImages, setSelectedImages] = useState([]);
  const [selectedQuotes, setSelectedQuotes] = useState({});
  const [showPopup, setShowPopup] = useState(false);
  const [currentImage, setCurrentImage] = useState(null);

  useEffect(() => {
    const randomNumber = Math.random();
    if (randomNumber < 0.2) {
      setSelectedImages([]);
    } else if (randomNumber < 0.5) {
      setSelectedImages(images.sort(() => 0.5 - Math.random()).slice(0, 2));
    } else {
      setSelectedImages([images[Math.floor(Math.random() * images.length)]]);
    }

    const newQuotes = {};
    images.forEach((image) => {
      newQuotes[image] = quotes[Math.floor(Math.random() * quotes.length)];
    });
    setSelectedQuotes(newQuotes);
  }, []);

  const handleImageClick = (image) => {
    const baseImageName = image.replace('-tiny', '');

    if (image.includes('-tiny')) {
        // Enlarge the tiny image
        setSelectedImages((prevImages) =>
            prevImages.map((img) => (img === image ? baseImageName : img))
        );
    } else {
        // Show the popup for the non-tiny image
        setCurrentImage(baseImageName);
        setShowPopup(true);
    }
};
  const handleClosePopup = () => {
    setShowPopup(false);
    setSelectedImages((prevImages) =>
      prevImages.map((img) => (img === currentImage ? currentImage + '-tiny' : img))
    );
  };

  return (
    <div className="RandomContentDisplay">
      {selectedImages.map((image, index) => (
        <img key={index} 
             src={`/characters/${image.replace('-tiny', '')}`} 
             alt={`Character ${index}`} 
             className={`characterImage ${image.includes('-tiny') ? 'tiny' : ''}`} 
             onClick={() => handleImageClick(image)} />
      ))}
      {showPopup && currentImage && (
        <div className="popup">
          <p className="popupText">{selectedQuotes[currentImage.replace('-tiny', '')]}</p>
          <button className="closePopup" onClick={handleClosePopup}>Close</button>
        </div>
      )}
    </div>
  );
}

export default RandomContentDisplay;
